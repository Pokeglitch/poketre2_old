	dw PersianSketch
	dw PersianSketch2
	
	dw GrowlitheChibi
	dw GrowlitheDoodle
	dw GrowlitheJapanese
	dw GrowlitheSketch
	dw GrowlitheSketch2
	
	dw ArcanineChibi
	dw ArcanineDoodle
