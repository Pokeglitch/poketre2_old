;checks to see what source we should use for the pokemon sprite
GetPokemonSpritePointer:
	ld a,d
	ld hl,W_MONHBACKSPRITE
	cp $d		;loading back sprite?
	jr nz,.loadFrontSprite

.loadOriginalSprite
	ld a,[hli]
	ld [W_SPRITEINPUTPTR],a    ; fetch sprite input pointer
	ld a,[hl]
	ld [W_SPRITEINPUTPTR+1],a
	ld a,[W_MONHSPRITEBANK]
	ld h,a
	ret
	
	
.loadFrontSprite
	ld hl,W_MONHFRONTSPRITE
	ld a,[wSpriteFilter]
	ld a,[wActiveCheats]
	bit SketchSpritesCheat,a		;load sketch sprite?
	jr z,.loadOriginalSprite		;load original sprite if not
		
	;get the index
	call .getDexNumber
	
	ld a,c
	ld hl,W_MONHFRONTSPRITE
	cp DEX_HUMAN
	jr nc,.loadOriginalSprite		;load the original sprite if the pokedex value is human or higher
	
	ld hl,AltSpritesTable
	
.loadSpriteFromTable
	dec c
	ld b,0		;bc = pokedex index
	add hl,bc
	add hl,bc
	add hl,bc		;hl now points to the start of the pokemon in the table
	
	ld a,[hli]
	ld [W_SPRITEINPUTPTR],a    ; fetch sprite input pointer
	ld a,[hli]
	ld [W_SPRITEINPUTPTR+1],a
	ld a,[hl]
	ld h,a
	ret
	
.getDexNumber
	ld a,[wd11e]
	push af
	ld a,[W_MONHDEXNUM]
	ld [wd11e],a
	predef IndexToPokedex
	ld hl,wd11e
	ld c, [hl]
	pop af
	ld [wd11e],a		;restore what d11e was originally
	ret
	
;Apply filters after tile is copied:
ApplyFilters:
	push de
	pop hl
	ld a,l
	bit 0,a
	inc hl
	jr z,.checkSwap		;just check the swap if the first byte
	dec hl
	dec hl
	ld a,[wSpriteFilter]
	bit HoloFilter,a		;holo?
	call nz,ApplyHoloFilter
	bit ShadowFilter,a		;shadow?
	call nz,ApplyShadowFilter
	bit InvisibleFilter,a		;invis?
	call nz,ApplyInvisibleFilter
	bit NightFilter,a		;night?
	call nz,ApplyNightFilter
	inc hl
	inc hl
.checkSwap
	ld a,[W_SPRITEFLIPPED]	;sprite flipped?
	and a
	jr z,.skipSwap	;dont swap if not
	swap [hl]
.skipSwap
	ret		;return
	
ApplyHoloFilter:
	ld b,8
	push af

.loop
	call .checkFirstBit	;check the first bit
	ld a,[hl]
	rrca
	ldi [hl],a
	ld a,[hl]
	rrca
	ldd [hl],a
	dec b
	jr nz,.loop
	
	pop af
	ret

.checkFirstBit
	bit 0,[hl]
	ret z
	inc hl
	bit 0,[hl]
	dec hl
	ret z		;return if bit boths are not set (i.e., color is not black)
	bit 1,[hl]
	jr nz,.skip1	;skip down if bit to left is set
	res 0,[hl]
.skip1
	inc hl
	bit 1,[hl]
	jr nz,.skip2		;skip if bit to left is set
	res 0,[hl]
.skip2
	dec hl
	ret

ApplyShadowFilter:
	ld b,8
	push af

.loop
	call .checkFirstBit	;check the first bit
	ld a,[hl]
	rrca
	ldi [hl],a
	ld a,[hl]
	rrca
	ldd [hl],a
	dec b
	jr nz,.loop
	
	pop af
	ret

.checkFirstBit
	bit 0,[hl]
	jr nz,.skip	;skip down if bit is set
	inc hl
	ld a,[hld]
	bit 0,a
	ret z		;return if 00
	set 0,[hl]
	inc hl
	set 0,[hl]
	dec hl
	ret		;otherwise, swap and return
.skip
	inc hl
	ld a,[hld]
	bit 0,a
	ret nz		;return if 11
	res 0,[hl]
	inc hl
	set 0,[hl]
	dec hl
	ret		;otherwise, swap and return
	
ApplyInvisibleFilter:
	
	ld b,8
	push af

.loop
	call .checkFirstBit	;check the first bit
	ld a,[hl]
	rrca
	ldi [hl],a
	ld a,[hl]
	rrca
	ldd [hl],a
	dec b
	jr nz,.loop
	
	pop af
	ret

.checkFirstBit
	bit 0,[hl]
	jr z,.setWhite		;set white if its not black
	inc hl
	bit 0,[hl]
	dec hl
	jr z,.setWhite		;set white if its not black
	bit 1,[hl]
	jr z,.setWhite		;set to white if the bit to left is not black
	inc hl
	bit 1,[hl]
	dec hl
	ret nz			;return if bit to the left is black
	
.setWhite
	res 0,[hl]
	inc hl
	res 0,[hl]
	dec hl
	ret
	
ApplyNightFilter:
	ld b,8
	push af

.loop
	call .checkFirstBit	;check the first bit
	ld a,[hl]
	rrca
	ldi [hl],a
	ld a,[hl]
	rrca
	ldd [hl],a
	dec b
	jr nz,.loop
	
	pop af
	ret

.checkFirstBit
	bit 0,[hl]
	jr nz,.skip	;skip down if bit is set
	inc hl
	ld a,[hld]
	bit 0,a
	jr z,.skip2		;return if 00
	set 0,[hl]
	inc hl
	set 0,[hl]
	dec hl
	ret		;otherwise, swap and return
.skip2
	set 0,[hl]
	inc hl
	res 0,[hl]
	dec hl
	ret		;otherwise, set to white
.skip
	inc hl
	ld a,[hld]
	bit 0,a
	ret nz		;return if 11
	res 0,[hl]
	inc hl
	set 0,[hl]
	dec hl
	ret		;otherwise, swap and return


